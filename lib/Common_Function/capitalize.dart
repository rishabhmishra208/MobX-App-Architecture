// ------ Capitalize first latter ------

String capitalize(String value) {
  if (value.isEmpty) {
    return value;
  }
  var result = value[0].toUpperCase();
  bool cap = true;
  for (int i = 1; i < value.length; i++) {
    if (value[i - 1] == ' ' && cap == true) {
      result = result + value[i].toUpperCase();
    } else {
      result = result + value[i];
      cap = false;
    }
  }
  return result;
}
